package foodxml;

public class Charges
{

	private String text;

	private Charge charge;

	public String getText()
	{

		return this.text;

	}

	public void setText(String arg)
	{

		this.text = arg;

	}

	public Charge getCharge()
	{

		return this.charge;

	}

	public void setCharge(Charge arg)
	{

		this.charge = arg;

	}

}
